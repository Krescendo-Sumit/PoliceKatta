package police.bharti.katta.model;

public class LiveTestQuestionModel {
    String id;
    String paperid;
    String papermaster_id;
    String questionno;
    String ans;
    String cdate;
    String details;
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPaperid() {
        return paperid;
    }

    public void setPaperid(String paperid) {
        this.paperid = paperid;
    }

    public String getPapermaster_id() {
        return papermaster_id;
    }

    public void setPapermaster_id(String papermaster_id) {
        this.papermaster_id = papermaster_id;
    }

    public String getQuestionno() {
        return questionno;
    }

    public void setQuestionno(String questionno) {
        this.questionno = questionno;
    }

    public String getAns() {
        return ans;
    }

    public void setAns(String ans) {
        this.ans = ans;
    }

    public String getCdate() {
        return cdate;
    }

    public void setCdate(String cdate) {
        this.cdate = cdate;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }


}
